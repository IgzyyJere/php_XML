Evidencija o posredovanju u prometu nekretnina
<br><br>
Članak 26.
<br><br>
(1) Posrednik mora voditi evidenciju o posredovanju u prometu nekretnina.<br><br>
(2) Evidencija iz stavka 1. Ovoga članka mora za svaki ugovor obuhvaćati sljedeće:<br><br>
– vrstu ugovora za čije sklapanje posreduje,<br>
– vrstu nekretnine (zemljište s oznakom namjene korištenja, stambeni I poslovni prostor I sl.),<br>
– tehničke podatke o nekretnini (sobnost, kvadratura I sl.),<br>
– cijenu, odnosno najamninu, zakupninu I sl. U ukupnome iznosu I jediničnome iznosu za m2 površine.<br><br>
(3) Posrednik je na zahtjev nadležnih tijela državne uprave dužan dati na uvid podatke iz stavka 2. Ovoga članka (ministarstvu nadležnom za financije I ministarstvu nadležnom za poslove graditeljstva) za potrebe provedbe zemljišne I stambene politike, tržišnog vrednovanja nekretnina I analize tržišta nekretnina.